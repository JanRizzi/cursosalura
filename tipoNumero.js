// tipo numero - exercicio para variaveis tipo numero

const primeiroNumero = 1;

const segundoNumero = 3;

const operacaoMatematica = primeiroNumero + segundoNumero;

console.log(operacaoMatematica);

console.log(primeiroNumero * segundoNumero);

console.log(operacaoMatematica / segundoNumero);

// ponto FLutuante

const pontoFlutuante = .5;

console.log(segundoNumero * pontoFlutuante);

// Nan Não é um numero

const alura = "alura";

const operacaoNan = primeiroNumero * alura;

console.log(operacaoNan);

// divisão por 0

const numeroZero = 0;

console.log(primeiroNumero / 0);

console.log(numeroZero / 0);

console.log(primeiroNumero + segundoNumero + operacaoMatematica + pontoFlutuante + numeroZero);
